CREATE TABLE HRIS_AUTO_NOTIFICATION(
	NOTIFICATION_HEAD_ID INT,
	NOTIFICATION_HEAD_NAME INT,
	FREQUENCY CHAR(1),
	LAST_EXECUTED_ON DATE,
	EMAIL_FLAG CHAR(1),
	SYSTEM_NOTIFICATION_FLAG CHAR(1),
	PRIMARY KEY (NOTIFICATION_HEAD_ID)
);

CREATE TABLE HRIS_AUTO_NOTIFICATION_QUERY(
NOTIFICATION_QUERY_ID INT,
NOTIFICATION_HEAD_ID INT,
QUERY VARCHAR(4000),
PRIORITY_INDEX INT,
PRIMARY KEY (NOTIFICATION_QUERY_ID),
FOREIGN KEY (NOTIFICATION_HEAD_ID) REFERENCES HRIS_AUTO_NOTIFICATION (NOTIFICATION_HEAD_ID)
);


