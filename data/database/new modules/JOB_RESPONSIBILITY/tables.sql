CREATE TABLE HRIS_JOB_RESPONSIBILITY (
    ID DECIMAL(7) PRIMARY KEY,
    JOB_RES_ENG_NAME VARCHAR(100),
    JOB_RES_NEP_NAME VARCHAR(255),
    JOB_RES_ENG_DESCRIPTION VARCHAR(500),
    JOB_RES_NEP_DESCRIPTION VARCHAR(1000),
    STATUS CHAR(1),
    CONSTRAINT JOB_RES_STS_CHK CHECK(STATUS IN ('E','D')),
    CREATED_BY DECIMAL(7),
    CREATED_DT DATE,
    MODIFIED_BY DECIMAL(7),
    MODIFIED_DT DATE,
    DELETED_BY DECIMAL(7),
    DELETED_DT DATE
);

CREATE TABLE HRIS_EMPLOYEE_JOB_RESPONSIBILITY_ASSIGN (
    ID DECIMAL(7) PRIMARY KEY,
    EMPLOYEE_ID DECIMAL(7),
    JOB_RESPONSIBILITY_ID DECIMAL(7),
    REMARKS VARCHAR(255),
    STATUS CHAR(1),
    CONSTRAINT EMP_JOB_RES_ASS_STS_CHK CHECK(STATUS IN ('E','D')),
    CREATED_BY DECIMAL(7),
    CREATED_DT DATE,
    MODIFIED_BY DECIMAL(7),
    MODIFIED_DT DATE,
    DELETED_BY DECIMAL(7),
    DELETED_DT DATE
);

alter table HRIS_EMPLOYEE_JOB_RESPONSIBILITY_ASSIGN
add (assigned_by decimal(7), start_date date, end_date date);